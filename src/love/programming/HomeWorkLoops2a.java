package love.programming;

public class HomeWorkLoops2a {

    static int i,j;
    public static void main(String[] args) {
        for(i=0;i<=5;i++)
        {
            for(j=0;j<i;j++)
            {
                System.out.print("* ");
            }
            System.out.println();
        }

    }
}
